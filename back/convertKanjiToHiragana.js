const dirPath = path.join(__dirname, 'src/domain/data');
const files = fs.readdirSync(dirPath).filter(f => f.endsWith('Exercises.json'));

let totalReplacements = 0;

files.forEach(file => {
  const filePath = path.join(dirPath, file);
  let content = fs.readFileSync(filePath, 'utf8');
  let replacements = 0;

  // Sort by length descending to replace longer strings first
  const sortedKanjiPairs = Object.entries(kanjiToHiragana).sort((a, b) => b[0].length - a[0].length);

  sortedKanjiPairs.forEach(([kanji, hiragana]) => {
    const regex = new RegExp(kanji.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'g');
    const matches = content.match(regex) || [];
    if (matches.length > 0) {
      replacements += matches.length;
      content = content.replace(regex, hiragana);
    }
  });

  if (replacements > 0) {
    fs.writeFileSync(filePath, content, 'utf8');
    console.log(`${file}: ${replacements} replacements`);
    totalReplacements += replacements;
  }
});

console.log(`\nTotal replacements: ${totalReplacements}`);
